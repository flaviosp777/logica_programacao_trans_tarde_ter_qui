// Exibir na tela os números de 0 até 50.

// 01 - Apenas While
int i = 0;

while (i <= 50)
{
    Console.WriteLine(i);
    i++;
}

// 02 - Do While

int a = 0;

do
{
    Console.WriteLine(a);
    a++;
} while (a <= 50);

// 03- Pelo For 

for(int f = 0; f <= 50; f++)
{
    Console.WriteLine(f);
}


